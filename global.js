var btnTop = document.getElementById('m-top');

window.setInterval(function() {

    setTimeout(function(){

        btnTop.style.transform = "scale(0.8)";
    
        setTimeout(function(){
            btnTop.style.transform = "scale(0.9)";
        },500);
    
    },700);

}, 1000);

